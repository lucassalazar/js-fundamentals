const a = 7 // = Simbolo de atribuição
let b = 3

b += a // b = b + a (Atribuição aditiva)
console.log(b)

b -= 4 // b = b - a (Atribuição subtrativa)
console.log(b)

b *= 2 // b = b * 2 (Atribuição multiplicativa)
console.log(b)

b /= 2 // b = b / 2 (Atribuição divisiva)
console.log(b)

b %= 2 // b = b % 2 (Atribuição modular)
console.log(b)